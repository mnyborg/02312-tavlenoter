package lektion07;

public class Integer {
    private int val;

    public Integer(int val) {
        this.val = val;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public String toString() {
        return "Integer [val=" + val + "]";
    }
}
